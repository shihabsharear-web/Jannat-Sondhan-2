    fun generateMonthlyPrayerSchedule(): List<DayPrayerSchedule> {
        val days = mutableListOf<DayPrayerSchedule>()
        val baseTimes = listOf(
            Triple("তাহাজ্জুদ", "Tahajjud", Pair(90, 240)),
            Triple("ফরজ (ফজর)", "Farz (Fajr)", Pair(255, 325)),
            Triple("সূর্যোদয়", "Sunrise", Pair(330, 370)),
            Triple("ইশরাক", "Ishrak", Pair(375, 465)),
            Triple("চাশত", "Chasht", Pair(480, 705)),
            Triple("যোহর", "Zuhr", Pair(725, 960)),
            Triple("আসর", "Asr", Pair(975, 1110)),
            Triple("সূর্যাস্ত", "Sunset", Pair(1115, 1130)),
            Triple("মাগরিব", "Maghrib", Pair(1135, 1200)),
            Triple("আওয়াবিন", "Awabin", Pair(1205, 1245)),
            Triple("এশা", "Isha", Pair(1250, 1410))
        )

        for (day in 1..30) {
            val drift = (day - 15) / 2
            val dateLabel = "$day মে"

            val timingItems = baseTimes.map { (nameBn, nameEn, range) ->
                val startMin = range.first + drift
                val endMin = range.second + drift

                val start12 = minutesTo12Hour(startMin)
                val start24 = minutesTo24Hour(startMin)
                val end12 = minutesTo12Hour(endMin)
                val end24 = minutesTo24Hour(endMin)

                PrayerTimeItem(
                    nameBn = nameBn,
                    nameEn = nameEn,
                    startTime12h = start12,
                    startTime24h = start24,
                    endTime12h = end12,
                    endTime24h = end24
                )
            }

            days.add(DayPrayerSchedule(dateLabel, day, timingItems))
        }
        return days
    }

    private fun minutesTo12Hour(totalMinutes: Int): String {
        val norm = (totalMinutes + 1440) % 1440
        var hour = norm / 60
        val min = norm % 60
        val suffix = if (hour >= 12) "PM" else "AM"
        if (hour > 12) hour -= 12
        if (hour == 0) hour = 12
        return String.format("%02d:%02d %s", hour, min, suffix)
    }

    private fun minutesTo24Hour(totalMinutes: Int): String {
        val norm = (totalMinutes + 1440) % 1440
        val hour = norm / 60
        val min = norm % 60
        return String.format("%02d:%02d", hour, min)
    }

